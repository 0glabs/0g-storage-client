var hashCount *big.Int
var keyCount int
var addressCount *big.Int

func nextKey() []byte {
	keyCount += 1
	res := make([]byte, 0)
	for i := 0; i < keyCount; i++ {
		res = append(res, 65)
	}
	return res
}

func nextHash() common.Hash {
	return common.BigToHash(hashCount.Add(hashCount, big.NewInt(1)))
}

func nextAddress() common.Address {
	return common.BigToAddress(addressCount.Add(addressCount, big.NewInt(1)))
}

